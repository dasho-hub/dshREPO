subject = input()

if subject == "banana" \
    or subject == "apple" \
    or subject == "kiwi" \
    or subject == "cherry" \
    or subject == "lemon" \
    or subject == "grapes":
    print("fruit")
elif subject == "tomato" \
    or subject == "cucumber" \
    or subject == "pepper" \
    or subject == "carrot":
    print("vegetable")
else:
    print("unknown")